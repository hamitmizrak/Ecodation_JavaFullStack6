package com.hamitmizrak.javasetutorials;

public class _11_DoWhile {

    public static void main(String[] args) {
        // Conditional
        // Loop
        // Date
        // Method
        // Try-catch

        System.out.println("For");
        //For
        for (int i = 1; i <=10 ; i++) {
            System.out.print(i+" ");
        }

        System.out.println("\nWHILE");

        int k = 1;
        //while
        while(k <=10 ){
            System.out.print(k+" ");
            k++;
        }

        System.out.println("\nDO-WHILE");
        int m = 1;  ;
        do{
            System.out.print(m+" ");
            m++;
        }while(m <=10);

    }
}
